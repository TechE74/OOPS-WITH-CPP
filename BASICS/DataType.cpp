// THIS PROGRAM HELPS US TO UNDERSTAND TAKING INPUT FROM USER 

#include<bits/stdc++.h>

using namespace std;


int main(){
    int num1;    // (integer) Variable ( Container which is used to store data of specific type ) ( either 2 or 4 byte )
    char ch;     // (character) Variable (size 1 byte)
    double num2; // (double) Variable (size 8 byte) 
    float num3;  // (float) Variable (size 4 byte)

    
    cout<<"Enter Integer value : ";// prompt for user
    cin>>num1; // take input from user
    cout<<"User input : "<<num1<<endl; // check for value by user

    cout<<"Enter Character value : ";// prompt for user
    cin>>ch; // take input from user
    cout<<"User input : "<<ch; // check for value by user

    cout<<"Enter Double value : ";// prompt for user
    cin>>num2; // take input from user
    cout<<"User input : "<<num2; // check for value by user 

    cout<<"Enter Float value : ";// prompt for user
    cin>>num3; // take input from user
    cout<<"User input : "<<num3; // check for value by user

}